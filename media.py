# Faça um programa que as 4 notas bimestaris e mostre a média

nota1 = float(input('\n Digite a primeira nota do aluno: '))
nota2 = float(input('\n Digite a segunda nota do aluno: '))
nota3 = float(input('\n Digite a terceira nota do aluno: '))
nota4 = float(input('\n Digite a quarta nota do aluno: '))

media = ((nota1 + nota2 + nota3 + nota4)/4)

print('\n A media do aluno é: {}'.format(media))